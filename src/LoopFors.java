public class LoopFors
{
    public static void main(String[] args)
    {   
        int second = 10;
        for (int first = 5; first < second; first = first + 2)
        {
            System.out.println(first);
            // code block to be executed
        }

        System.out.println();

        // //   return 0;
    }    
}
